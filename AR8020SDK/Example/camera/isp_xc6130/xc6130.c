int32_t XC6130_WriteReg(uint16_t u16_xc6130Reg, uint8_t u8_xc6130Val)
{
    uint8_t u8_buf[3] = {0};
    int32_t s32_result = 0;

    u8_buf[0] = u16_xc6130Reg >> 8;
    u8_buf[1] = u16_xc6130Reg & 0xff;
    u8_buf[2] = u8_xc6130Val;
    
    I2C_Master_WriteData(XC6130_COMPONENT, XC6130_I2C_ADDR, u8_buf, 3);
    s32_result = I2C_Master_WaitTillIdle(XC6130_COMPONENT, XC6130_I2C_MAX_DELAY);
    if(0 != s32_result)
    {
        DLOG_Error("write reg error:reg=%x,val=%x", u16_xc6130Reg, u8_xc6130Val);
        s32_result = -1;
    }
    else
    {
        ;
    }

    return s32_result;
}

int32_t XC6130_ReadReg(uint16_t u16_xc6130Reg, uint8_t *pu8_xc6130Val)
{
    uint8_t u8_regBuf[2] = {0};
    uint8_t u8_rdVal = 0;
    int32_t s32_result = 0;
    
    u8_regBuf[0] = u16_xc6130Reg >> 8;
    u8_regBuf[1] = u16_xc6130Reg & 0xff;

    
    I2C_Master_ReadData(XC6130_COMPONENT, XC6130_I2C_ADDR, u8_regBuf,2, &u8_rdVal, 1);
    s32_result = I2C_Master_WaitTillIdle(XC6130_COMPONENT, XC6130_I2C_MAX_DELAY);
    if(0 != s32_result) 
    {
        DLOG_Error("read reg error:reg=%x", u16_xc6130Reg);
        s32_result = -1;
    }
    else
    {
        ;
    }   

    *pu8_xc6130Val = u8_rdVal;

    return s32_result;
}

int32_t XC6130_DownloadFirmware(STRU_CAMERA_REG_VALUE *pst_xc6130, uint32_t u32_xc6130ArySize)
{
    register uint16_t u16_regAddr = 0;
    register uint8_t u8_val = 0;
    uint8_t u8_regVal = 0;
    uint32_t u32_i;
    int32_t s32_retVal = 0;

    for (u32_i = 0; u32_i < u32_xc6130ArySize; ++u32_i, ++pst_xc6130) 
    {
        u16_regAddr = pst_xc6130->u16_regAddr;
        u8_val = pst_xc6130->u8_val;

        s32_retVal = XC6130_WriteReg(u16_regAddr, u8_val);
        if (s32_retVal < 0)
        {
            goto err;
        }

    }
err:
    return s32_retVal;
}

